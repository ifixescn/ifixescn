/** 根据语言代码和键名获取翻译，回退到英文，再回退到键名本身 */
export function getTranslation(langCode: string, key: string, fallback?: string): string {
  // 先尝试精确匹配
  const exact = TRANSLATIONS[langCode]?.[key];
  if (exact) return exact;

  // 尝试主语言代码 (zh-CN -> zh)
  const mainCode = langCode.split("-")[0];
  const byMain = Object.keys(TRANSLATIONS).find(
    (l) => l.toLowerCase().startsWith(mainCode.toLowerCase()) && l !== "en"
  );
  if (byMain && TRANSLATIONS[byMain][key]) return TRANSLATIONS[byMain][key];

  // 回退到英文
  const english = TRANSLATIONS["en"]?.[key];
  if (english) return english;

  // 最后回退到 fallback 或 key 本身
  return fallback || key;
}
